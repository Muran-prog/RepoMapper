#!/usr/bin/env node
/**
 * dump-worldcover-ramp.mjs — emit a gdaldem color-relief table for the
 * ESA WorldCover landcover-tint pipeline, parsed straight out of
 * `src/style/worldcover-ramps.js` so the offline raster build always
 * matches what the live MapLibre paint would render.
 *
 * Usage
 * -----
 *   tools/dump-worldcover-ramp.mjs [--theme=light|dark]
 *                                  [--output=path.txt]
 *
 *   tools/dump-worldcover-ramp.mjs --list-classes
 *
 * Output (gdaldem `-color-relief` syntax):
 *
 *     # ESA WorldCover landcover ramp (theme=light)
 *     10  58  92  60 255         # Tree cover
 *     20 168 178  98 255         # Shrubland
 *     ...
 *     80   0   0   0   0         # Permanent water — transparent
 *     ...
 *     nv   0   0   0   0         # NoData
 *
 * The "nv" line is the gdaldem token for NoData. We always include it
 * so partial tile coverage (e.g. coastline approximations missing a
 * row of pixels) doesn't leak black into the output.
 *
 * Mapping `value → token` is taken from `WORLDCOVER_RAMPS[theme]` —
 * never hard-coded here. Adding a class is one edit to one file.
 *
 * Dependencies: only Node ≥ 18, no npm packages.
 */

import { fileURLToPath, pathToFileURL } from 'node:url';
import path from 'node:path';
import fs from 'node:fs/promises';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.dirname(HERE);

async function importEsm(rel) {
  const abs = path.resolve(REPO, rel);
  return import(pathToFileURL(abs).href);
}

function parseArgs(argv) {
  const out = { theme: 'light', output: null, listClasses: false };
  for (const a of argv) {
    if (a === '--list-classes') out.listClasses = true;
    else if (a.startsWith('--theme=')) out.theme = a.slice('--theme='.length);
    else if (a.startsWith('--output=')) out.output = a.slice('--output='.length);
    else if (a === '-h' || a === '--help') {
      printHelp();
      process.exit(0);
    } else {
      process.stderr.write(`unknown argument: ${a}\n`);
      process.exit(2);
    }
  }
  return out;
}

function printHelp() {
  process.stdout.write(
    `dump-worldcover-ramp.mjs — emit gdaldem color-relief table for ESA WorldCover

  tools/dump-worldcover-ramp.mjs [--theme=light|dark] [--output=path.txt]
  tools/dump-worldcover-ramp.mjs --list-classes
  -h, --help

The mapping value→colour is read from src/style/worldcover-ramps.js.
`,
  );
}

/**
 * Format a single (value, R, G, B, A) line for gdaldem. Values are
 * right-aligned in 3-char columns so the resulting file diffs cleanly.
 *
 * @param {string|number} value 'nv' for NoData, or the integer class value.
 * @param {number} r 0..255
 * @param {number} g 0..255
 * @param {number} b 0..255
 * @param {number} a 0..255
 * @param {string} [comment]
 * @returns {string}
 */
function formatLine(value, r, g, b, a, comment) {
  const v = String(value).padStart(3);
  const rs = String(r).padStart(3);
  const gs = String(g).padStart(3);
  const bs = String(b).padStart(3);
  const as = String(a).padStart(3);
  const tail = comment ? `  # ${comment}` : '';
  return `${v} ${rs} ${gs} ${bs} ${as}${tail}\n`;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));

  const {
    WORLDCOVER_CLASSES,
    WORLDCOVER_CLASS_NAMES,
    getWorldcoverRamp,
    hexToRgba,
  } = await importEsm('src/style/worldcover-ramps.js');

  if (args.listClasses) {
    for (const v of WORLDCOVER_CLASSES) {
      process.stdout.write(`${String(v).padStart(3)}  ${WORLDCOVER_CLASS_NAMES[v]}\n`);
    }
    return;
  }

  if (!['light', 'dark'].includes(args.theme)) {
    process.stderr.write(`unknown theme '${args.theme}' (expected 'light' or 'dark')\n`);
    process.exit(2);
  }

  const ramp = getWorldcoverRamp(args.theme);
  const lines = [];
  lines.push(`# ESA WorldCover 10 m 2021 v200 — landcover-tint ramp\n`);
  lines.push(`# theme: ${args.theme}\n`);
  lines.push(`# generated by tools/dump-worldcover-ramp.mjs\n`);
  lines.push(`# DO NOT edit this file directly — change src/style/worldcover-ramps.js instead\n`);

  for (const value of WORLDCOVER_CLASSES) {
    const hex = ramp[value];
    if (!hex) {
      process.stderr.write(`worldcover-ramps.js missing colour for class ${value}\n`);
      process.exit(2);
    }
    const rgba = hexToRgba(hex);
    if (!rgba) {
      process.stderr.write(`worldcover-ramps.js bad colour for class ${value}: ${hex}\n`);
      process.exit(2);
    }
    const [r, g, b, a] = rgba;
    const name = WORLDCOVER_CLASS_NAMES[value] ?? '';
    const note = hex === 'transparent' ? `${name} — transparent` : name;
    lines.push(formatLine(value, r, g, b, a, note));
  }
  // NoData sentinel for gdaldem. Anything unmatched → fully transparent.
  lines.push(formatLine('nv', 0, 0, 0, 0, 'NoData'));

  const text = lines.join('');
  if (args.output) {
    await fs.writeFile(args.output, text);
    process.stderr.write(`wrote ${args.output}\n`);
  } else {
    process.stdout.write(text);
  }
}

main().catch((err) => {
  process.stderr.write(`dump-worldcover-ramp.mjs crashed: ${err?.stack || err}\n`);
  process.exit(2);
});
